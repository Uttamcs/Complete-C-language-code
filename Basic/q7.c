#include<stdio.h>
int main()
{
    float a,b;
    printf("Enter the value of a:");
    scanf("%f",&a);
    printf("Enter the value of b:");
    scanf("%f",&b);
    printf("The average of two number is %f",(a+b)/2);
    return 0;
}